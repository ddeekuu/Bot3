package com.vegazsdev.bobobot.core;

public class ForwardMethod {

    private final Class aClass;
    private String alias;

    public ForwardMethod(Class aClass, String alias) {
        this.aClass = aClass;
    }

    public Class getaClass() {
        return aClass;
    }

    public String getAlias() {
        return alias;
    }

}
